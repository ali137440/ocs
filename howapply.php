    <div id="collapseThree" class="collapse" aria-labelledby="headingThree" data-parent="#accordion">
      <div class="card-body">
        <p>A step-by-step guide to complete the registration process is given below:</p>
<h3>Step 1: Registration</h3>
<ul>
  <li>Visit the official website wbjeeb.in</li>
   <li>Click on “JECA”</li>
   <li>Click on “Online Application Form JECA 2020”</li>
   <li>Click on “New Candidate Registration”</li>
   <li>Click on “I agree and Proceed” on the instructions page</li>
   <li>Fill the entair Form with appropied information</li>
   <li>Click on Submit.</li>
   <li>An application number will be generated.</li>
</ul>
<h3>Step 2: Application Form</h3>
<ul>
  <li>After registering, click on Fill in Application Form</li>
   <li>ll in the following details asked in the application form</li>
  <li>Click on Save & Next</li>
   
</ul>
<h3>Step 3: Uploading the Documents</h3>
<ul>
  <li>The Photograph must be recently taken so that it matches with the candidate’s physical features.</li>
   <li>Low quality images are not acceptable and are likely to be rejected.</li>
  <li>Background of the photograph must be of very light colour.</li>
   <li>The face of the candidate should occupy at least 50% of the entire space for photograph looking straight to the camera.</li>
   <li>Spectacles of dark or tinted glasses are not acceptable.</li>
</ul>
<h3>Step 4: Payment of Application fee</h3>
<ul>
  <li>WB JECA 2020 Application fee is of INR 500/- for candidates of all categories.</li>
   <li>After uploading of images candidates will be directed automatically to the fee payment step.</li>
  <li>The application fee can only be paid online using Netbanking/ Debit card/ Credit card.</li>
   <li>The fee once paid is not refundable under any circumstances.</li>
   
</ul>
<h3>Step 5: Confirmation page</h3>
<ul>
  <li>On successful completion of all the above steps, candidate will automatically be directed to the option for downloading the ‘Confirmation Page’.</li>
   <li>APPLICATION IS NOT COMPLETE UNTIL THE CONFIRMATION PAGE IS GENERATED.</li>
  <li>The confirmation page contains information as given by the candidate. As such, the Board is in no way responsible for any mistake in it. </li>
   <li>Candidate should take a Print-out of the Confirmation Page and should preserve the same carefully till the completion of admission process.</li>
   
</ul>
